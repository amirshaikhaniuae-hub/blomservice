/* ── Apply translations to DOM ──────────────────────────────────────── */
function applyLang(lang) {
  const dict = BLOM_I18N[lang] || BLOM_I18N.en;

  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    const key = el.dataset.i18n;
    const val = dict[key];
    if (!val) return;

    // Strip any HTML tags to get plain text
    const plain = val.replace(/<[^>]+>/g, ' ').replace(/\s+/g, ' ').trim();

    // Always use textContent — safe and simple
    el.textContent = plain;
  });

  document.documentElement.lang = lang;
  if (dict['page.title']) document.title = dict['page.title'];

  const sel = document.getElementById('lang-select');
  if (sel) sel.value = lang;

  try { localStorage.setItem('blom_lang', lang); } catch(_) {}
}

/* ── Switcher UI logic ──────────────────────────────────────────────── */
function initLangSwitcher() {
  const sel = document.getElementById('lang-select');
  if (!sel) return;

  // Auto-detect language
  let saved = null;
  try { saved = localStorage.getItem('blom_lang'); } catch(_) {}
  const browserLang = (navigator.language || 'en').slice(0, 2).toLowerCase();
  const initLang = (saved && BLOM_I18N[saved]) ? saved : (BLOM_I18N[browserLang] ? browserLang : 'en');

  sel.value = initLang;
  applyLang(initLang);

  sel.addEventListener('change', function() {
    applyLang(sel.value);
  });
}

// Run immediately if DOM is ready, otherwise wait
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initLangSwitcher);
} else {
  initLangSwitcher();
}
